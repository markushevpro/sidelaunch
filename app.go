func systemTray( a *App ) func() {
	return func() {
		systray.SetIcon( IconData )
		systray.SetTitle( "Sidelaunch" )

		systray.SetOnClick(func(menu systray.IMenu) {
			menu.ShowMenu()
		})

		systray.SetOnRClick(func(menu systray.IMenu) {
			menu.ShowMenu()
		})
		
		systray.CreateMenu()

		mQuit := systray.AddMenuItem("Close", "Exit application")
		mQuit.Enable()
		mQuit.Click(func() {
			os.Exit(0)
		})
	}
}

func (a *App) startup(ctx context.Context) {
	a.ctx = ctx

    if ( a.page == "" ) {
        runtime.WindowSetSize( ctx, 0, 0 )

        runtime.OnFileDrop(a.ctx, func(x, y int, paths []string) {
            runtime.EventsEmit(a.ctx, "filedrop", paths )
        })

        systray.Run(systemTray(a), func() {})
    }

}

